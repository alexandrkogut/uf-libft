#include <libft.h>

int		ft_atoi(const char *str)
{
	int		i;
	int		z;
	int		n;

	i = 0;
	z = 1;
	n = 0;
	while ((*(str + i) == '\n') || (*(str + i) == '\t') || (*(str + i) == '\r')
		|| (*(str + i) == '\v') || (*(str + i) == '\f') || (*(str + i) == ' '))
		i++;
	if (*(str + i) == '-')
		z = -1;
	if (*(str + i) == '+' || *(str + i) == '-')
		i++;
	while (*(str + i) && ft_isdigit((int)*(str + i)))
		n = n * 10 + (*(str + i++) - '0');
	return (z * n);
}
